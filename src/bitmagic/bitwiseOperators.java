package bitmagic;

public class bitwiseOperators {
     static int bitwiseAnd(int a, int b){
         return a &b;
     }

     static int bitwiseOr(int a, int b){
         return a | b;
     }

     static int bitwiseXor(int a, int b){
         return a ^ b;
     }

     static int bistwiseNot(int a){
         return ~a;
     }

     static int bitwiseLeftShift(int a, int b){
         return a << b;
     }
}
